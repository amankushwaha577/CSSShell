// 1. CSS (Cascading Style Sheets) :
// Definition: CSS is the standard language used to style HTML documents. 
//             It controls the layout, colors, fonts, and overall appearance of web pages.
// 
// Syntax: CSS uses a straightforward syntax with selectors and declarations. 

// Example:
// body {
//     background-color: blue;
//     color: white;
// }

// Features: CSS has evolved over time to include features like Flexbox and Grid layouts, animations, and transitions.
//  However, it lacks advanced functionalities like variables, nesting, and mixins.




// 2. SASS (Syntactically Awesome Style Sheets)
// Definition: SASS is a preprocessor scripting language that extends CSS. It allows for more dynamic styling capabilities.
// Syntax: SASS can be written in two syntaxes:

// A. Indented Syntax: This uses indentation instead of curly braces and semicolons, making it less verbose. Example:
// Example :
// body
//   background-color: blue
//   color: white

// B. SCSS (Sassy CSS): This is the more popular syntax of SASS that uses curly braces and semicolons, making it look more like standard CSS. Example:
// Example :
// body {
//     background-color: blue;
//     color: white;
// }

// Features: SASS provides variables, nesting, mixins, inheritance, and functions, allowing for more organized and reusable code.




// SCSS (Sassy CSS)
// Definition: SCSS is a syntax of SASS (its a part of SASS oh cool) that is fully compliant with CSS. Any valid CSS is also valid SCSS, which means you can gradually adopt SCSS features in your existing CSS code.
// Syntax: As mentioned, SCSS retains the familiar CSS syntax, making it easy for developers to transition from CSS to SCSS.

// Features: All features of SASS are available in SCSS, including nesting, variables, mixins, and more.







// Summary
// 1. CSS: The foundational styling language for the web, limited in functionality.
// 2. SASS: A powerful preprocessor that offers two syntaxes (Indented and SCSS) and advanced features for writing styles.
// 3. SCSS: A syntax of SASS that is more CSS-like and widely used, allowing for easier integration with existing CSS.


